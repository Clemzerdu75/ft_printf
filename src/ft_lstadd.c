#include "libft.h"

void	ft_lstadd(t_list **alst, t_list *new)
{
	if (new && *alst)
	{
		new->next = *alst;
		*alst = new;
	}
	else
	{
		*alst = new;
		(*alst)->next = NULL;
	}
}
